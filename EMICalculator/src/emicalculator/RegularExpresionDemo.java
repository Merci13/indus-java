    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try {
            JLabel asteric = new JLabel("*");
        //regular expresion para el movil
        //"^\d{8}$"
        // para Java, la expresion "\d" debe ser con dos backslash => "\\d"
        String regularExpresionMovil = "^\\d{8}$";
        Pattern a = Pattern.compile(regularExpresionMovil);
        String source1 = txtMovil.getText();
        Matcher m1 = a.matcher(source1);
        if (m1.find()) {
            lblName.setText("The Movil number is Correct!");
        } else {

            lblName.setText("The Movil number isn't Correct!");
            lblName.setForeground(Color.red);
            txtMovil.setText(" ");

            asteric.setForeground(Color.red);
            txtMovil.add(asteric);
        }

        //regular expresion para le nombre
        //"^[a-zA-Z]*$"
        String regularExpresionName = "^[a-zA-Z ]*$";
        Pattern b = Pattern.compile(regularExpresionName);
        String source = txtName.getText();
        Matcher m2 = b.matcher(source);
        if (m2.find()) {
            lblName.setText(lblName.getText() + "\n" + "Given Name is Correct");

        } else {

            lblName.setText("Invalid Name Entered!");
        }

        //regular expresion para email
        //"^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$"
        String regularExpresionEmail = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\\\.[A-Z]{2,6}$";
        Pattern c = Pattern.compile(regularExpresionEmail, Pattern.CASE_INSENSITIVE);
        String source3 = txtEmail.getText();
        Matcher m3 = c.matcher(source3);
        if (m3.find()) {
            lblName.setText(lblName.getText() + "\n" + "The Email Address is Correct!");
        } else {
            lblName.setText("The Email Address isÂ´nt Correct");
        }

        } catch (Exception e) {
            lblError.setText(e.getMessage());
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String mystory = "";
        String findThis = "";
        String replaceWith = "";

        try {
            mystory = txtArea.getText();
            findThis = txtFind.getText();
            replaceWith = txtReplaceWith.getText();

            String regularExpresionReplace = findThis;
            Pattern p4 = Pattern.compile(regularExpresionReplace);
            Matcher m4 = p4.matcher(mystory);
            String newStory = m4.replaceAll(replaceWith);
            txtArea.setText(newStory);

        } catch (Exception e) {

            lblError.setText(e.getMessage());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

